package com.shathamurish.myschool2.Help.HomeAdapter;

public class NewsHelper {

    int image;
    String title;

    public NewsHelper(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
